// Helper function to translate Chinese text to English
export function translateChineseToEnglish(text: string): string {
  let translatedText = text;
  
  // Try exact match first
  if (chineseToEnglishMap[text]) {
    return chineseToEnglishMap[text];
  }
  
  // Try partial replacements
  for (const [chinese, english] of Object.entries(chineseToEnglishMap)) {
    if (translatedText.includes(chinese)) {
      translatedText = translatedText.replace(new RegExp(chinese, 'g'), english);
    }
  }
  
  return translatedText;
}
